void repeat(int c, int count) {
	for (int i = 0; i < count; i++)
		printf("%c", c);
}

void mainFrame(int16_t x1, int16_t y1, int16_t x2, int16_t y2) {
	gotoxy(x1, y1);
	printf("%c", 201);
	repeat(205, (x2 - x1 - 2));
	printf("%c", 187);
	for (int i = y1 + 1; i < y2 - 1; i++) {
		gotoxy(x1, i);
		printf("%c", 186);
		gotoxy(x2 - x1, i);
		printf("%c\n", 186);
	}
	printf("%c", 200);
	repeat(205, (x2 - x1 - 2));
	printf("%c", 188);
}
